// ─── Lazy loader — fetches country tours on demand ───────────────
// Usage: const tours = await loadCountryTours('italy')
export async function loadCountryTours(countrySlug) {
  try {
    const mod = await import(`./tours-by-country/${countrySlug}.js`);
    return mod.default || [];
  } catch(e) {
    console.warn('Tours not found for:', countrySlug);
    return [];
  }
}

// ─── Sync helpers (metadata only, no tour data needed) ───────────
export function getCountry(id) {
  return WORLD_COUNTRIES.find(c => c.id === id) || null;
}

export function getCountriesByRegion(region) {
  if (!region || region === 'All') return WORLD_COUNTRIES;
  return WORLD_COUNTRIES.filter(c => c.region === region);
}

export function getFeaturedCountries(n = 12) {
  return WORLD_COUNTRIES.slice(0, n);
}
