export function getTour(slug){return toursData[slug]||null;}
export function getPackage(slug){return packagesData[slug]||null;}
export function getAllTours(){return Object.values(toursData);}
export function getTourPackages(tourSlug){
  const tour=toursData[tourSlug];
  if(!tour)return[];
  return tour.packages.map(s=>packagesData[s]).filter(Boolean);
}
export function getRelatedTours(currentSlug,limit=3){
  return Object.values(toursData).filter(t=>t.slug!==currentSlug).slice(0,limit);
}